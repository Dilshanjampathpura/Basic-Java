public class VariableTypesExample {
    
    public static void main(String[] args) {
        
        int INTEGER;
        double FLOAT;
        String STRING;
        boolean BOOLEAN;
        
        INTEGER = 30;
        FLOAT = 9.81;
        STRING = "Static variable";
        BOOLEAN = false;

        System.out.println("Integer variable: " + INTEGER);
        System.out.println("Float variable: " + FLOAT);
        System.out.println("String variable: " + STRING);
        System.out.println("Boolean variable: " + BOOLEAN);
    }
}
